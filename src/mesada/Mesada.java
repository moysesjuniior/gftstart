package mesada;

public class Mesada {
    public float pagar;
    public float sacar;
    public float saldo;

    public float getPagar() {
        return pagar;
    }

    public void setPagar(float pagar) {
        this.pagar = pagar;
    }

    public float getSacar() {
        return sacar;
    }

    public void setSacar(float sacar) {
        this.sacar = sacar;
    }

    public float getSaldo() {
        return saldo;
    }

    public void setSaldo(float saldo) {
        this.saldo = saldo;
    }
}
